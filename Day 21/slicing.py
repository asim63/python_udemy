list = [1,2,3,4,5,6,7,8]

#prints all 
print(list[:])

#prints between 1 and 8
print(list[1:-1])

#prints upto last skipping the first element
print(list[1:])

#last element
print(list[-1])

#up to 5
print(list[:5])

#from section 2 to 5 but skipping elements by 2
print(list[2:5:2]) #gives [3,5]

#from first to last skipping by 2
print(list[::2])

#from reverse the list
print(list[::-1])